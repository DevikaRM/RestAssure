package pojo2;

public class Data {
	
		private int id;
		private String name;
		private int year;
		private int color;
		private int pantone_value;
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public int getYear() {
			return year;
		}
		public void setYear(int year) {
			this.year = year;
		}
		public int getColor() {
			return color;
		}
		public void setColor(int color) {
			this.color = color;
		}
		public int getPantone_value() {
			return pantone_value;
		}
		public void setPantone_value(int pantone_value) {
			this.pantone_value = pantone_value;
		}

}
